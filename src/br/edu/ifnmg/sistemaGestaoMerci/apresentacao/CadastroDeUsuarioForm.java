    private void excluirUsuarioPesquisado() {
        try {

            int resposta;
            String mensagem = "Deseja excluir Usuário? " + usuario.getLogin();
            String titulo = "Exclusão de Usuário";
            resposta = JOptionPane.showConfirmDialog(this, mensagem, titulo, JOptionPane.YES_NO_OPTION);

            if (resposta == JOptionPane.YES_NO_OPTION) {
                UsuarioBO usuarioBo = new UsuarioBO();
                usuarioBo.removerUsuario(usuario);
                inicializarCamposTela();
                JOptionPane.showMessageDialog(this, "Usuário Excluido com sucesso!", "Pesquisa Exclusão de Usuário", JOptionPane.INFORMATION_MESSAGE);
                limparCamposTela();
            }
        } catch (Exception e) {
            String mensagem = "Erro inesperado! Informe a mensagem de erro ao administrador do sistema.";
            mensagem += "\nMensagem de erro:\n" + e.getMessage();
            JOptionPane.showMessageDialog(this, mensagem, "Exclusão de Usuário ", JOptionPane.ERROR_MESSAGE);
            this.dispose();
        }
    }

    private void pesquisarUsuarioCadastrado() throws SQLException {
        Usuario usuarioBuscado = new Usuario();
        usuarioBuscado.setLogin(txtLogin.getText().trim());

        UsuarioBO usuarioBO = new UsuarioBO();
        usuario = usuarioBO.pesquisarLoginCadastrado(usuarioBuscado);
        inicializarCamposTela();

    }

    private void limparCamposTela() {
        this.txtNome.setText("");
        this.txtLogin.setText("");
        this.txtSenha.setText("");
        if (chbCaixeiro.isSelected()) {
            this.chbCaixeiro.setSelected(false);
        } else if (chbGerente.isSelected()) {
            this.chbGerente.setSelected(false);
        } else if (chbGestorCompras.isSelected()) {
            this.chbGestorCompras.setSelected(false);
        } else {
            this.chbGestorEstoque.setSelected(false);
        }
    }

    private void recuperarCamposTela() throws ParseException {
        if (!txtNome.getText().trim().equals("")) {
            usuario.setNome(txtNome.getText().trim());
        } else {
            throw new CampoObrigatorioException();
        }
        if (!txtLogin.getText().trim().equals("")) {
            usuario.setLogin(txtLogin.getText().trim());
        } else {
            throw new CampoObrigatorioException();
        }
        if (!txtSenha.getText().trim().equals("")) {
            usuario.setSenha(txtSenha.getText().trim());
        } else {
            throw new CampoObrigatorioException();
        }
        if (chbCaixeiro.isSelected()) {
            usuario.setGrupo("CAIXEIRO");
        } else if (chbGerente.isSelected()) {
            usuario.setGrupo("GERENTE");
        } else if (chbGestorCompras.isSelected()) {
            usuario.setGrupo("GESTOR DE COMPRAS");
        } else if (chbGestorEstoque.isSelected()) {
            usuario.setGrupo("GESTOR DE ESTOQUE");
        } else {
            throw new CampoObrigatorioException();
        }

    }

