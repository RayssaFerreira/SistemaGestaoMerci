    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        if (usuarioEmEdicao == false) {
            salvarUsuario();
        } else {
            editarUsuario();
        }

    }//GEN-LAST:event_btnSalvarActionPerformed

    private void editarUsuario() {
        try {
            this.recuperarCamposTela();

            UsuarioBO usuarioBO = new UsuarioBO();
            usuarioBO.alterarUsuario(usuario);
            JOptionPane.showMessageDialog(this,
                    "Usuário alterado com sucesso!",
                    "Alteração de Usuário",
                    JOptionPane.INFORMATION_MESSAGE);
            this.limparCamposTela();
            listagemUsuarioForm.carregaTabelaUsuario();
            btnSalvar.setEnabled(false);

        } catch (SistemaGestaoException e) {
            JOptionPane.showMessageDialog(
                    this,
                    e.getMessage(),
                    "Alteração de Usuário",
                    JOptionPane.ERROR_MESSAGE
            );
            btnSalvar.setEnabled(true);

        } catch (Exception e) {
            System.out.println("Erro inesperado! Informe a mensagem de erro ao administrador do sistema.");
            e.printStackTrace(System.out);
            JOptionPane.showMessageDialog(
                    this,
                    "Erro inesperado! Informe o erro ao administrador do sistema",
                    "Alteração de Usuário",
                    JOptionPane.ERROR_MESSAGE
            );
        }
    }

    private void salvarUsuario() {
        try {
            this.recuperarCamposTela();

            UsuarioBO usuarioBO = new UsuarioBO();
            usuarioBO.inserirUsuario(usuario);
            JOptionPane.showMessageDialog(this,
                    "Usuário cadastrado com sucesso!",
                    "Cadastro de Usuário",
                    JOptionPane.INFORMATION_MESSAGE);
            this.limparCamposTela();
            btnSalvar.setEnabled(false);

        } catch (SistemaGestaoException e) {
            JOptionPane.showMessageDialog(
                    this,
                    e.getMessage(),
                    "Cadastro de Usuário",
                    JOptionPane.ERROR_MESSAGE
            );
            btnSalvar.setEnabled(true);

        } catch (Exception e) {
            System.out.println("Erro inesperado! Informe a mensagem de erro ao administrador do sistema.");
            e.printStackTrace(System.out);
            JOptionPane.showMessageDialog(
                    this,
                    "Erro inesperado! Informe o erro ao administrador do sistema",
                    "Cadastro de Usuário",
                    JOptionPane.ERROR_MESSAGE
            );
        }
    }
